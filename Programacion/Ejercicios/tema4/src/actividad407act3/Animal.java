package actividad407act3;

public class Animal {
	
	// Atributos
	
	protected String nombre;
	protected int edad;
	protected char sexo;
	protected double peso;
	
	// Constructor
	
	public Animal(String nombre, int edad, char sexo, double peso) {
		this.nombre = nombre;
		this.edad = edad;
		this.sexo = sexo;
		this.peso = peso;
	}
	
	// Methods
	
}
